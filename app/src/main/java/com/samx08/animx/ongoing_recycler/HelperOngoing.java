package com.samx08.animx.ongoing_recycler;

public class HelperOngoing
{
    int image;
    String text;

    public HelperOngoing(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
