package com.samx08.animx.recyclerview_watch;

public class WatchHelper {

    int image;
    String text;

    public WatchHelper(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
